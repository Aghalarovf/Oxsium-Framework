//    • IP cameras / NVR          (CAMERA)
//    • Smart-TV / streaming      (TV)
//    • IoT / smart-home          (IOT)
//    • VoIP phones               (VOIP)
//    • Virtual machines          (VM)
//    • Generic / unknown         (UNKNOWN)
//
//  Usage:
//    #include "oui_database.h"
//    OuiResult r = oui_lookup("70:5F:A3:xx:xx:xx");
//    r.vendor      → "Xiaomi Communications Co Ltd"
//    r.device_type → DeviceType::PHONE
//    r.type_str()  → "Phone"
// ─────────────────────────────────────────────────────────────────────────────
#include <string>
#include <algorithm>
#include <cctype>

// ─────────────────────────────────────────────────────────────────────────────
//  DeviceType enum
// ─────────────────────────────────────────────────────────────────────────────
enum class DeviceType : uint8_t {
    UNKNOWN  = 0,
    PHONE,       // smartphones
    TABLET,      // tablets (iPad, Galaxy Tab, etc.)
    PC,          // laptops, desktops, servers
    ROUTER,      // routers, firewalls, gateways
    SWITCH,      // managed/unmanaged switches
    AP,          // wireless access points
    PRINTER,     // printers, MFPs
    CAMERA,      // IP cameras, NVRs, doorbells
    TV,          // smart-TVs, streaming sticks, set-top boxes
    IOT,         // smart-home, wearables, embedded devices
    VOIP,        // VoIP desk phones, conference devices
    VM,          // virtual machine / hypervisor vNIC
};

inline const char* device_type_str(DeviceType t) {
    switch (t) {
        case DeviceType::PHONE:   return "Phone";
        case DeviceType::TABLET:  return "Tablet";
        case DeviceType::PC:      return "PC";
        case DeviceType::ROUTER:  return "Router";
        case DeviceType::SWITCH:  return "Switch";
        case DeviceType::AP:      return "AccessPoint";
        case DeviceType::PRINTER: return "Printer";
        case DeviceType::CAMERA:  return "Camera";
        case DeviceType::TV:      return "TV";
        case DeviceType::IOT:     return "IoT";
        case DeviceType::VOIP:    return "VoIP";
        case DeviceType::VM:      return "VM";
        default:                  return "Unknown";
    }
}

