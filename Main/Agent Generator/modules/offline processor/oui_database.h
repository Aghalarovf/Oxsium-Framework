// ─────────────────────────────────────────────────────────────────────────────
//  OuiResult  — returned by oui_lookup()
// ─────────────────────────────────────────────────────────────────────────────
struct OuiResult {
    std::string vendor;                    // vendor string, empty if not found
    DeviceType  device_type = DeviceType::UNKNOWN;

    const char* type_str() const { return device_type_str(device_type); }
    bool        found()    const { return !vendor.empty(); }
};

// ─────────────────────────────────────────────────────────────────────────────
//  oui_lookup(mac)
//
//  Accepts any common MAC format:
//    "70:5F:A3:xx:xx:xx"
//    "70-5F-A3-xx-xx-xx"
//    "705FA3xxxxxx"
//    "705fa3xxxxxx"
//
//  Returns OuiResult with vendor + device_type.
//  If no match is found, result.vendor is empty and type is UNKNOWN.
// ─────────────────────────────────────────────────────────────────────────────
inline OuiResult oui_lookup(const std::string& mac) {
    OuiResult res;
    if (mac.size() < 6) return res;

    // Normalize: strip separators, uppercase, take first 6 hex chars (3 bytes)
    std::string oui;
    oui.reserve(6);
    for (unsigned char c : mac) {
        if (c == ':' || c == '-' || c == '.') continue;
        oui += static_cast<char>(std::toupper(c));
        if (oui.size() == 6) break;
    }
    if (oui.size() < 6) return res;

    for (int i = 0; OUI_DB[i].oui != nullptr; ++i) {
        // Skip entries whose stored key has spaces (data-entry typos) or
        // wrong length — guard against bad entries in the table.
        const char* key = OUI_DB[i].oui;
        // Quick length check: stored keys should be exactly 6 chars
        size_t klen = 0;
        while (key[klen] && key[klen] != ' ') ++klen;
        if (klen != 6) continue;

        if (oui.compare(0, 6, key, 6) == 0) {
            res.vendor      = OUI_DB[i].vendor;
            res.device_type = OUI_DB[i].device_type;
            return res;
        }
    }
    return res;  // not found
}